static /**
        * @brief Case-insensitive string comparison helper
        */
    int
    strcasecmp_portable(const char *s1, const char *s2, int *out_diff) {
  if (!out_diff)
    return EINVAL;
  if (!s1 || !s2) {
    *out_diff = -1;
    return EINVAL;
  }
  while (*s1 && *s2) {
    int diff = tolower((unsigned char)*s1) - tolower((unsigned char)*s2);
    if (diff != 0) {
      *out_diff = diff;
      return 0;
    }
    s1++;
    s2++;
  }
  *out_diff = tolower((unsigned char)*s1) - tolower((unsigned char)*s2);
  return 0;
}

static /**
        * @brief Autogenerated docstring
        */
    int
    sprintf_s_wrapper(char *buf, size_t start, size_t cap, const char *fmt,
                      ...) {
  int written;
  va_list args;
  va_start(args, fmt);
#if defined(_MSC_VER) && !defined(__INTEL_COMPILER)
  written = vsprintf_s(buf + start, cap - start, fmt, args);
#else
  (void)cap; /* Unused in C89 fallback */
  written = vsprintf(buf + start, fmt, args);
#endif
  va_end(args);
  return written;
}

