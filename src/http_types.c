static size_t urlencode_len(const char *src) {
  size_t len = 0;
  const char *p;
  if (!src)
    return 0;
  for (p = src; *p; p++) {
    unsigned char c = (unsigned char)*p;
    if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
        (c >= '0' && c <= '9') || c == '-' || c == '_' || c == '.' ||
        c == '~') {
      len++;
    } else if (c == ' ') {
      len++;
    } else {
      len += 3;
    }
  }
  return len;
}

static void urlencode_append(char **dest, const char *src) {
  static const char *hex = "0123456789ABCDEF";
  const char *p;
  char *q = *dest;
  if (!src)
    return;
  for (p = src; *p; p++) {
    unsigned char c = (unsigned char)*p;
    if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
        (c >= '0' && c <= '9') || c == '-' || c == '_' || c == '.' ||
        c == '~') {
      *q++ = c;
    } else if (c == ' ') {
      *q++ = '+';
    } else {
      *q++ = '%';
      *q++ = hex[c >> 4];
      *q++ = hex[c & 15];
    }
  }
  *dest = q;
}

