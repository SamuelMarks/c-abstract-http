/**
 * @brief Simple base64 encoder for basic auth
 */
static int base64_encode(const unsigned char *src, size_t len, char **out) {
  static const char b64[] =
      "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
  char *res;
  size_t olen = 4 * ((len + 2) / 3);
  size_t i, j;
  if (!src || !out)
    return EINVAL;
  res = (char *)malloc(olen + 1);
  if (!res)
    return ENOMEM;

  for (i = 0, j = 0; i < len;) {
    unsigned int octet_a = i < len ? src[i++] : 0;
    unsigned int octet_b = i < len ? src[i++] : 0;
    unsigned int octet_c = i < len ? src[i++] : 0;
    unsigned int triple = (octet_a << 0x10) + (octet_b << 0x08) + octet_c;

    res[j++] = b64[(triple >> 3 * 6) & 0x3F];
    res[j++] = b64[(triple >> 2 * 6) & 0x3F];
    res[j++] = b64[(triple >> 1 * 6) & 0x3F];
    res[j++] = b64[(triple >> 0 * 6) & 0x3F];
  }

  if (len % 3 == 1) {
    res[olen - 1] = '=';
    res[olen - 2] = '=';
  } else if (len % 3 == 2) {
    res[olen - 1] = '=';
  }
  res[olen] = '\0';
  *out = res;
  return 0;
}

